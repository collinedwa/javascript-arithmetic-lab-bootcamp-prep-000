function add(a, b){
  return a + b
}

function subtract(a, b){
  return a - b
}

function multiply(a, b){
  return a * b
}

function divide (a, b){
  return a / b
}

function inc(n){
  return ++n
}

function dec(n){
  return --n
}

function makeInt(n){
  return parseInt(n)
}

add (1, 2)
subtract (10, 5)
multiply (2, 2)
divide(500, 100)
inc(10)
dec(10)
makeInt("10")
makeInt("10", 10)